import * as React from "react";
import { cn } from "../../lib/utils";

export interface CardProps extends React.HTMLAttributes<HTMLDivElement> {
  variant?: "default" | "outline" | "ghost";
}

const cardVariants: Record<NonNullable<CardProps["variant"]>, string> = {
  default: "bg-white dark:bg-gray-900 border-gray-200 dark:border-gray-800 shadow-sm",
  outline: "bg-transparent border-gray-300 dark:border-gray-700",
  ghost: "bg-transparent border-transparent shadow-none"
};

const Card = React.forwardRef<HTMLDivElement, CardProps>(
  ({ className, variant = "default", ...props }, ref) => (
    <div
      ref={ref}
      className={cn(
        "rounded-lg border text-gray-900 dark:text-gray-100",
        cardVariants[variant],
        className
      )}
      {...props}
    />
  )
);
Card.displayName = "Card";

